static void SymbolSynchronizer_symbolSyncCodegen(const emlrtStack *sp,
  comm_SymbolSynchronizer *obj, creal32_T x[100000], emxArray_creal32_T *y,
  boolean_T *overflowFlag, real32_T *sampleIdx)
{
  static real32_T fv[12] = { 0.0F, -0.5F, 0.5F, 0.0F, 1.5F, -0.5F, 1.0F, -0.5F,
    -0.5F, 0.0F, -0.5F, 0.5F };

  emlrtStack st;
  creal32_T fcv[12];
  creal32_T xSeq[4];
  creal32_T b_x[3];
  creal32_T b_y[3];
  creal32_T intOut;
  real_T d;
  int32_T c_sampleIdx;
  int32_T i;
  int32_T k;
  real32_T b_sampleIdx;
  real32_T e;
  real32_T t1_im;
  real32_T t2_im;
  real32_T t2_re;
  boolean_T d_x[10];
  boolean_T c_x[9];
  boolean_T b_overflowFlag;
  boolean_T c_y;
  boolean_T exitg1;
  boolean_T exitg2;
  obj->pNumStrobe = 0.0F;
  b_overflowFlag = false;
  b_sampleIdx = 1.0F;
  c_sampleIdx = 0;
  exitg1 = false;
  while ((!exitg1) && (c_sampleIdx < 100000)) {
    b_sampleIdx = (real32_T)c_sampleIdx + 1.0F;
    if ((obj->pNumStrobe == 11000.0F) && obj->pStrobe) {
      b_overflowFlag = true;
      exitg1 = true;
    } else {
      obj->pNumStrobe += (real32_T)obj->pStrobe;
      st.site = &y_emlrtRSI;
      xSeq[0].re = x[c_sampleIdx].re;
      xSeq[0].im = x[c_sampleIdx].im;
      for (i = 0; i < 3; i++) {
        xSeq[i + 1].re = obj->pInterpFilterState[i].re;
        xSeq[i + 1].im = obj->pInterpFilterState[i].im;
      }

      for (k = 0; k < 12; k++) {
        fcv[k].re = fv[k];
        fcv[k].im = 0.0F;
      }

      for (k = 0; k < 3; k++) {
        b_y[k].re = 0.0F;
        b_y[k].im = 0.0F;
        for (i = 0; i < 4; i++) {
          b_y[k].re += fcv[k + 3 * i].re * xSeq[i].re - fcv[k + 3 * i].im *
            xSeq[i].im;
          b_y[k].im += fcv[k + 3 * i].re * xSeq[i].im + fcv[k + 3 * i].im *
            xSeq[i].re;
        }
      }

      e = obj->pMu;
      b_x[0].re = b_y[0].re;
      b_x[0].im = b_y[0].im;
      b_x[1].re = obj->pMu * b_y[1].re;
      b_x[1].im = obj->pMu * b_y[1].im;
      b_x[2].re = e * e * b_y[2].re;
      b_x[2].im = e * e * b_y[2].im;
      intOut = sumColumnB(b_x);
      for (i = 0; i < 3; i++) {
        obj->pInterpFilterState[i].re = xSeq[i].re;
        obj->pInterpFilterState[i].im = xSeq[i].im;
      }

      if (obj->pStrobe) {
        d = obj->pNumStrobe;
        k = (int32_T)(real32_T)emlrtIntegerCheckR2012b(d, &emlrtDCI, (void *)sp);
        obj->pSymbolHolder[emlrtDynamicBoundsCheckR2012b(k, 1, 11000, &emlrtBCI,
          (void *)sp) - 1].re = intOut.re;
        d = obj->pNumStrobe;
        k = (int32_T)(real32_T)emlrtIntegerCheckR2012b(d, &emlrtDCI, (void *)sp);
        obj->pSymbolHolder[emlrtDynamicBoundsCheckR2012b(k, 1, 11000, &emlrtBCI,
          (void *)sp) - 1].im = intOut.im;
      }

      if (obj->pNumStrobe > 11000.0F) {
        b_overflowFlag = true;
        exitg1 = true;
      } else {
        st.site = &ab_emlrtRSI;
        if (obj->pStrobe) {
          for (k = 0; k < 9; k++) {
            c_x[k] = !obj->pStrobeHistory[k + 1];
          }

          c_y = true;
          k = 0;
          exitg2 = false;
          while ((!exitg2) && (k < 9)) {
            if (!c_x[k]) {
              c_y = false;
              exitg2 = true;
            } else {
              k++;
            }
          }

          if (c_y) {
            e = obj->pTEDBuffer[5].re;
            t1_im = obj->pTEDBuffer[5].im;
            t2_re = obj->pTEDBuffer[5].re;
            t2_im = obj->pTEDBuffer[5].im;
            t2_re += e;
            e = t1_im + t2_im;
            if (e == 0.0F) {
              t1_im = t2_re / 2.0F;
              e = 0.0F;
            } else if (t2_re == 0.0F) {
              t1_im = 0.0F;
              e /= 2.0F;
            } else {
              t1_im = t2_re / 2.0F;
              e /= 2.0F;
            }

            t2_im = obj->pTEDBuffer[0].re;
            t2_re = obj->pTEDBuffer[0].im;
            e = t1_im * (muSingleScalarSign(t2_im) - muSingleScalarSign
                         (intOut.re)) + e * (muSingleScalarSign(t2_re) -
              muSingleScalarSign(intOut.im));
          } else {
            e = 0.0F;
          }
        } else {
          e = 0.0F;
        }

        for (k = 0; k < 9; k++) {
          d_x[k] = obj->pStrobeHistory[k + 1];
        }

        d_x[9] = obj->pStrobe;
        i = (int32_T)d_x[0];
        for (k = 0; k < 9; k++) {
          i += (int32_T)d_x[k + 1];
        }

        switch (i) {
         case 0:
          break;

         case 1:
          for (k = 0; k < 9; k++) {
            obj->pTEDBuffer[k].re = obj->pTEDBuffer[k + 1].re;
            obj->pTEDBuffer[k].im = obj->pTEDBuffer[k + 1].im;
          }

          obj->pTEDBuffer[9].re = intOut.re;
          obj->pTEDBuffer[9].im = intOut.im;
          break;

         default:
          for (k = 0; k < 8; k++) {
            obj->pTEDBuffer[k].re = obj->pTEDBuffer[k + 2].re;
            obj->pTEDBuffer[k].im = obj->pTEDBuffer[k + 2].im;
          }

          obj->pTEDBuffer[8].re = 0.0F;
          obj->pTEDBuffer[8].im = 0.0F;
          obj->pTEDBuffer[9].re = intOut.re;
          obj->pTEDBuffer[9].im = intOut.im;
          break;
        }

        st.site = &bb_emlrtRSI;
        t2_re = obj->pLoopPreviousInput + obj->pLoopFilterState;
        t1_im = e * obj->pProportionalGain + t2_re;
        obj->pLoopFilterState = t2_re;
        obj->pLoopPreviousInput = e * obj->pIntegratorGain;
        st.site = &cb_emlrtRSI;
        for (k = 0; k < 9; k++) {
          obj->pStrobeHistory[k] = obj->pStrobeHistory[k + 1];
        }

        obj->pStrobeHistory[9] = obj->pStrobe;
        obj->pStrobe = (obj->pNCOCounter < t1_im + 0.1F);
        if (obj->pStrobe) {
          t2_im = obj->pNCOCounter;
          obj->pMu = t2_im / (t1_im + 0.1F);
        }

        t2_im = obj->pNCOCounter - (t1_im + 0.1F);
        if (muSingleScalarIsNaN(t2_im) || muSingleScalarIsInf(t2_im)) {
          e = rtNaNF;
        } else if (t2_im == 0.0F) {
          e = 0.0F;
        } else {
          e = muSingleScalarRem(t2_im, 1.0F);
          if (e == 0.0F) {
            e = 0.0F;
          } else if (t2_im < 0.0F) {
            e++;
          }
        }

        obj->pNCOCounter = e;
        c_sampleIdx++;
      }
    }
  }

  e = obj->pNumStrobe;
  if (e < 1.0F) {
    i = -1;
  } else {
    k = (int32_T)(real32_T)emlrtIntegerCheckR2012b((real_T)e, &b_emlrtDCI, (void
      *)sp);
    i = emlrtDynamicBoundsCheckR2012b(k, 1, 11000, &b_emlrtBCI, (void *)sp) - 1;
  }

  k = y->size[0];
  y->size[0] = i + 1;
  emxEnsureCapacity_creal32_T1(y, k);
  for (k = 0; k <= i; k++) {
    y->data[k].re = obj->pSymbolHolder[k].re;
    y->data[k].im = obj->pSymbolHolder[k].im;
  }

  *overflowFlag = b_overflowFlag;
  *sampleIdx = b_sampleIdx;
}

static creal32_T sumColumnB(creal32_T x[3])
{
  creal32_T y;
  int32_T k;
  y.re = x[0].re;
  y.im = x[0].im;
  for (k = 0; k < 2; k++) {
    y.re += x[k + 1].re;
    y.im += x[k + 1].im;
  }

  return y;
}

